// --- Language Application Logic ---

function applyLanguage(lang) {
    document.querySelectorAll('[data-key]').forEach(element => {
        const key = element.getAttribute('data-key');
        if (translations[lang] && translations[lang][key]) {
            // Handle placeholders for input fields
            if (element.tagName === 'INPUT') {
                element.placeholder = translations[lang][key];
            } else {
                element.innerHTML = translations[lang][key];
            }
        }
    });
}

// Global logic for pages to sync
window.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('userLanguage') || 'en-IN';
    
    // Set the dropdown value if it exists on the current page
    const langSelect = document.getElementById('dashboard-lang') || document.getElementById('login-lang');
    if (langSelect) {
        langSelect.value = savedLang;
        
        // Add listener for language change
        langSelect.addEventListener('change', (e) => {
            const newLang = e.target.value;
            localStorage.setItem('userLanguage', newLang);
            applyLanguage(newLang);
        });
    }

    applyLanguage(savedLang);
});